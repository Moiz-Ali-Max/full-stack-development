let a = 2
console.log(a)

a = 3
console.log(a)

//Now use const (constant in JS - a variable that cannot be reassigned/changed)
const a1 = 2
console.log(a1)
// a1 = 3 // This will give an error

//Now we use variable using var (old way of declaring variables)
var c = 2
console.log(c) 
c = 3
console.log(c) //Value of c can be changed. But var has some issues, so it is not recommended to use var


